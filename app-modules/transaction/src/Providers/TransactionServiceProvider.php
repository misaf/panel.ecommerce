<?php

declare(strict_types=1);

namespace Misaf\Transaction\Providers;

use Illuminate\Contracts\Foundation\Application;
use Illuminate\Support\Facades\Event;
use Illuminate\Support\ServiceProvider;
use Misaf\Transaction\Listeners\TransactionTransferSubscriber;
use Misaf\Transaction\Listeners\WithdrawalLimitSubscriber;
use Misaf\Transaction\Services\TransactionService;

final class TransactionServiceProvider extends ServiceProvider
{
    /**
     * @return void
     */
    public function register(): void
    {
        $this->app->bind('transaction-service', fn(Application $app) => new TransactionService());
    }

    /**
     * @return void
     */
    public function boot(): void
    {
        $this->loadTranslationsFrom(__DIR__ . '/../../lang', 'transaction');

        $this->publishes([
            __DIR__ . '/../../lang' => $this->app->langPath('vendor/transaction'),
        ], 'transaction-lang');

        Event::subscribe(TransactionTransferSubscriber::class);
        Event::subscribe(WithdrawalLimitSubscriber::class);
    }

    /**
     * @return array<int, string>
     */
    public function provides(): array
    {
        return [
            'transaction-service',
        ];
    }
}
